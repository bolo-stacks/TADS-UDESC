"""
Loop condicional WHILE
Loop básico com WHILE
que é uma repetição condicional, ou seja,
repete enquanto verdadeira
"""

# Imprime os números em sequência até 100 dentro do loop

# Declara a variável
num = 1

# Começa o loop da contagem até 100
while num <= 100:
    print(num)
    num = num + 1



